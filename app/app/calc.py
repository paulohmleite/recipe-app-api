"""
Calculator functions
"""


def add(x, y):
    """
    Add x and y return result.
    :param x:
    :param y:
    :return x + y:
    """
    return x + y


def subtract(x, y):
    """
    Subtract x from y and return result
    :param x:
    :param y:
    :return result:
    """
    return y - x
